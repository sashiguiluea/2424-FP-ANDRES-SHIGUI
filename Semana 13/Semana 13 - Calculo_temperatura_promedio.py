def calcular_temperatura_promedio(matriz_temperaturas):
    """
    Función para calcular el promedio de temperaturas de varias ciudades a lo largo de varias semanas.

    Args:
    matriz_temperaturas (list): Lista de listas que contiene las temperaturas por ciudad, semana y día.

    Returns:
    dict: Diccionario con el promedio de temperatura de cada ciudad.
    """
    promedios = {}  # Diccionario para almacenar el promedio de temperatura de cada ciudad

    # Iterar sobre cada ciudad en la matriz
    for i, ciudad in enumerate(matriz_temperaturas):  # 'i' es el índice de la ciudad
        total_temperatura = 0  # Inicializar el total de temperatura para cada ciudad
        num_dias = 0  # Inicializar el contador de días

        # Recorremos cada semana de la ciudad
        for semana in ciudad:
            # Recorremos cada día de la semana
            for dia in semana:
                total_temperatura += dia["temp"]  # Sumar la temperatura de cada día
                num_dias += 1  # Incrementar el número de días

        # Calculamos el promedio de temperatura de la ciudad si hay días registrados
        if num_dias > 0:
            promedio = total_temperatura / num_dias  # Calcular el promedio dividiendo el total entre el número de días
        else:
            promedio = 0  # Si no hay días, el promedio es 0 (aunque esto es poco probable con la estructura de datos)

        # Almacenar el promedio en el diccionario con el nombre de la ciudad
        promedios[f"Ciudad {i + 1}"] = promedio  # 'i + 1' porque las ciudades empiezan a contarse desde 1
    return promedios  # Devolver el diccionario con los promedios


